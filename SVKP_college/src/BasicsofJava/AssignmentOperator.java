package BasicsofJava;

public class AssignmentOperator
{

	public static void main(String[] args)
	{
		int a=15;
		//Add and assign 
		a+=5;//a=a+5
		System.out.println("a after +=" +a);//20
		//Sub and assign
		a-=5;//a=a-5
		System.out.println("a after -=" +a);//10
		//Multiply and assign
		a*=4;//a=a*4
		System.out.println("a after *=" +a);//60
		//Division and assign
		a/=5;//=a/5
		System.out.println("a after /=" +a);//12
		//Modulus and assign
		a%=8;//a=a%8
		System.out.println("a after %=" +a);//4
		}
}
